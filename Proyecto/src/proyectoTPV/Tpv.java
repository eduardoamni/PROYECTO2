    private void btnCobrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCobrarActionPerformed
        DefaultTableModel mActu = (DefaultTableModel) jTable1.getModel();
        try {
            for (DefaultTableModel m : arrayModelos) {
                if (m.equals(mActu)) {
                    if (lblTotal.getText().equals("0.00 €")) {
                        arrayModelos.remove(m);
                    } else {
                        if(verCantidad()){
                        restarCantidad();
                        crearFactura();
                        crearOperacionesF();

                        arrayModelos.remove(m);
                        }else{
                            javax.swing.JOptionPane.showMessageDialog(this, "Cantidad de producto no valida ", "Error", javax.swing.JOptionPane.PLAIN_MESSAGE);
                        }
                    }
                }
            }
        } catch (Exception ex) {
        }

        try {
            jTable1.setModel(arrayModelos.get(0));

            m = (DefaultTableModel) jTable1.getModel();
            verTotal();

        } catch (Exception ex) {
            try {
                borrarFilas();
                verTotal();
            } catch (Exception exx) {
            }

        }
        
        mSub(evt);
    }//GEN-LAST:event_btnCobrarActionPerformed

